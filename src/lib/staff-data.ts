export const getRoleColor = (role: string) => {
  const colors: Record<string, string> = {
    Owner: "bg-purple-500/20 text-purple-700 dark:text-purple-300 border-purple-500/50",
    Manager: "bg-blue-500/20 text-blue-700 dark:text-blue-300 border-blue-500/50",
    Server: "bg-green-500/20 text-green-700 dark:text-green-300 border-green-500/50",
    Chef: "bg-orange-500/20 text-orange-700 dark:text-orange-300 border-orange-500/50",
    Bartender: "bg-amber-500/20 text-amber-700 dark:text-amber-300 border-amber-500/50",
    Host: "bg-cyan-500/20 text-cyan-700 dark:text-cyan-300 border-cyan-500/50",
  }
  return colors[role] || "bg-gray-500/20 text-gray-700 dark:text-gray-300 border-gray-500/50"
}

export const getStatusColor = (status: string) => {
  const colors: Record<string, string> = {
    available: "bg-green-500 text-white",
    busy: "bg-red-500 text-white",
    break: "bg-yellow-500 text-white",
    offline: "bg-gray-400 text-white",
    clocked_out: "bg-blue-400 text-white",
  }
  return colors[status] || "bg-gray-500 text-white"
}

export const getWorkloadColor = (workload: number) => {
  if (workload < 50) return "text-green-600 dark:text-green-400"
  if (workload < 80) return "text-yellow-600 dark:text-yellow-400"
  return "text-red-600 dark:text-red-400"
}
